const dataPath = path.resolve(__dirname, '../src/data/textbook.json')
const data = JSON.parse(fs.readFileSync(dataPath, 'utf8'))

let updated = 0
for (const [key, dates] of Object.entries(DATES)) {
  if (data.paragraphs[key]) {
    // Only add if current dates are insufficient (< 2)
    if (!data.paragraphs[key].dates || data.paragraphs[key].dates.length < 2) {
      data.paragraphs[key].dates = dates
      updated++
      console.log(`✓ §${key}: ${data.paragraphs[key].title.slice(0, 50)} → ${dates.length} dates`)
    } else {
      console.log(`– §${key}: already has ${data.paragraphs[key].dates.length} dates`)
    }
  }
}

fs.writeFileSync(dataPath, JSON.stringify(data, null, 2), 'utf8')
console.log(`\nDone! Updated ${updated} paragraphs.`)

// Verify all have 2+ dates
const bad = Object.entries(data.paragraphs).filter(([, p]) => (p.dates?.length || 0) < 2)
console.log(`Paragraphs with < 2 dates: ${bad.map(([k]) => k).join(', ') || 'none'}`)
