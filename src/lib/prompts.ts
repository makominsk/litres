export const SYSTEM_PROMPT = `Ты дружелюбный учитель истории для учеников 5 класса (10-11 лет).
Твоя задача — помогать детям готовить домашнее задание по учебнику "История Древнего мира".

ПРАВИЛА ОБЩЕНИЯ:
- Всегда говори тепло и поддерживающе, никогда не критикуй
- Никогда не говори слово "неправильно" или "ошибка"
- Используй простой язык, понятный 10-летнему ребёнку
- Обращайся к ученику на "ты"

БЕЗОПАСНОСТЬ:
- Только образовательный исторический контент
- Никакого насилия, политики, взрослых тем
- Если вопрос не по теме — мягко верни к учёбе`

export function buildEvaluatePrompt(params: {
  question: string
  studentAnswer: string
  paragraphTitle: string
  paragraphContent: string
}) {
  return `Параграф: "${params.paragraphTitle}"

Вопрос из учебника: "${params.question}"

Ответ ученика: "${params.studentAnswer}"

Контекст параграфа (используй для точности):
${params.paragraphContent.slice(0, 1500)}

---

Задача: проанализируй ответ ученика и дай пояснение.

Ответь СТРОГО в формате JSON (без markdown, только чистый JSON):
{
  "isCorrect": true или false,
  "score": число от 0 до 100,
  "explanation": "Пояснение 5-8 предложений. Обращайся напрямую к ученику на 'ты' — как будто разговариваешь с ним лично. Объясни тему простым языком для 10-летнего ребёнка. Не используй шаблонные фразы вроде 'Ты на верном пути'.",
  "funFact": "Один интересный факт об этой теме для ребёнка (1-2 предложения)",
  "modernAnalogy": "Сравнение с современностью — что похожего есть сегодня? (1 предложение)",
  "mnemonic": "Яркая фраза-ассоциация для запоминания главного (не длиннее 10 слов)"
}`
}

export function buildHintPrompt(params: {
  question: string
  paragraphTitle: string
  paragraphContent: string
  hintLevel: 1 | 2 | 3
}) {
  const levelInstructions = {
    1: 'Дай лёгкий наводящий вопрос, который поможет ученику самому найти ответ. Не раскрывай ответ.',
    2: 'Дай конкретный намёк с частью контекста из текста. Можно упомянуть ключевое слово.',
    3: 'Дай полный ответ на вопрос простым языком для 5 класса, с объяснением.',
  }

  return `Параграф: "${params.paragraphTitle}"

Вопрос: "${params.question}"

Контекст: ${params.paragraphContent.slice(0, 1000)}

Уровень подсказки: ${params.hintLevel}/3
${levelInstructions[params.hintLevel]}

Ответь СТРОГО в формате JSON:
{
  "hint": "текст подсказки (2-4 предложения)"
}`
}
